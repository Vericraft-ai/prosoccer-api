export const generateRandomValue = (attr: string) => {
  if (attr === 'age') {
    return Math.floor(Math.random() * 20) + 18;
  } else if (attr === 'height') {
    return (Math.floor(Math.random() * 50) + 150) / 100;
  } else if (attr === 'weight') {
    return Math.floor(Math.random() * 100) + 50;
  } else if (attr === 'position') {
    const positions = [
      'GK',
      'CB',
      'RB',
      'LB',
      'CDM',
      'CM',
      'CAM',
      'RM',
      'LM',
      'RW',
      'LW',
      'CF',
      'ST',
    ];
    return positions[Math.floor(Math.random() * positions.length)];
  } else if (attr === 'foot') {
    const feet = ['Left', 'Right'];
    return feet[Math.floor(Math.random() * feet.length)];
  } else if (attr === 'overall' || attr === 'potential') {
    return Math.floor(Math.random() * 100);
  } else {
    return Math.floor(Math.random() * 100);
  }
};

